create policy "authenticated users can read seasons"
on public.seasons for select to authenticated
using (true);

create policy "admins manage seasons"
on public.seasons for all to authenticated
using (public.is_admin())
with check (public.is_admin());

create policy "users read their driver application"
on public.driver_applications for select to authenticated
using (user_id = auth.uid() or public.is_admin());

create policy "users create their driver application"
on public.driver_applications for insert to authenticated
with check (user_id = auth.uid() and status = 'pending');

create policy "users resubmit a denied driver application"
on public.driver_applications for update to authenticated
using (user_id = auth.uid() and status = 'denied')
with check (user_id = auth.uid() and status = 'pending' and reviewed_by is null and reviewed_at is null);

create policy "admins update driver applications"
on public.driver_applications for update to authenticated
using (public.is_admin())
with check (public.is_admin());

create policy "recipients read their requests"
on public.distribution_requests for select to authenticated
using (owner_id = auth.uid());

create policy "approved drivers read available and assigned requests"
on public.distribution_requests for select to authenticated
using (
  public.is_approved_driver()
  and (
    status in ('approved', 'not_delivered')
    or assigned_driver_id = auth.uid()
  )
);

create policy "admins read all requests"
on public.distribution_requests for select to authenticated
using (public.is_admin());

create policy "recipients create their requests"
on public.distribution_requests for insert to authenticated
with check (owner_id = auth.uid() and status = 'submitted' and assigned_driver_id is null);

create policy "admins update requests"
on public.distribution_requests for update to authenticated
using (public.is_admin())
with check (public.is_admin());

create policy "request participants read delivery events"
on public.delivery_events for select to authenticated
using (
  public.is_admin()
  or actor_id = auth.uid()
  or exists (
    select 1 from public.distribution_requests request
    where request.id = request_id
      and (request.owner_id = auth.uid() or request.assigned_driver_id = auth.uid())
  )
);

create or replace function public.claim_delivery(target_request_id uuid)
returns public.distribution_requests
language plpgsql
security definer
set search_path = ''
as $$
declare
  claimed public.distribution_requests;
  previous_status public.request_status;
begin
  if auth.uid() is null or not public.is_approved_driver(auth.uid()) then
    raise exception 'Only approved drivers can claim deliveries';
  end if;

  select status into previous_status
  from public.distribution_requests
  where id = target_request_id
    and status in ('approved', 'not_delivered')
    and (assigned_driver_id is null or status = 'not_delivered')
  for update;

  update public.distribution_requests
  set assigned_driver_id = auth.uid(), status = 'driver_assigned'
  where id = target_request_id
    and status in ('approved', 'not_delivered')
    and (assigned_driver_id is null or status = 'not_delivered')
  returning * into claimed;

  if claimed.id is null then
    raise exception 'Delivery is no longer available';
  end if;

  insert into public.delivery_events (request_id, actor_id, event_type, from_status, to_status)
  values (claimed.id, auth.uid(), 'claimed', previous_status, 'driver_assigned');

  return claimed;
end;
$$;

create or replace function public.assign_delivery(target_request_id uuid, target_driver_id uuid)
returns public.distribution_requests
language plpgsql
security definer
set search_path = ''
as $$
declare
  assigned public.distribution_requests;
  previous_status public.request_status;
begin
  if not public.is_admin() then
    raise exception 'Only admins can assign deliveries';
  end if;

  if not public.is_approved_driver(target_driver_id) then
    raise exception 'The selected driver is not approved';
  end if;

  select status into previous_status
  from public.distribution_requests
  where id = target_request_id
    and status in ('approved', 'not_delivered')
  for update;

  update public.distribution_requests
  set assigned_driver_id = target_driver_id, status = 'driver_assigned'
  where id = target_request_id and status in ('approved', 'not_delivered')
  returning * into assigned;

  if assigned.id is null then
    raise exception 'Delivery is no longer available';
  end if;

  insert into public.delivery_events (request_id, actor_id, event_type, from_status, to_status, notes)
  values (assigned.id, auth.uid(), 'assigned', previous_status, 'driver_assigned', target_driver_id::text);

  return assigned;
end;
$$;

create or replace function public.unclaim_delivery(target_request_id uuid)
returns public.distribution_requests
language plpgsql
security definer
set search_path = ''
as $$
declare
  released public.distribution_requests;
begin
  update public.distribution_requests
  set assigned_driver_id = null, status = 'approved'
  where id = target_request_id
    and assigned_driver_id = auth.uid()
    and status = 'driver_assigned'
  returning * into released;

  if released.id is null then
    raise exception 'Only the assigned driver can unclaim this delivery';
  end if;

  insert into public.delivery_events (request_id, actor_id, event_type, from_status, to_status)
  values (released.id, auth.uid(), 'unclaimed', 'driver_assigned', 'approved');

  return released;
end;
$$;

create or replace function public.set_delivery_status(target_request_id uuid, next_status public.request_status)
returns public.distribution_requests
language plpgsql
security definer
set search_path = ''
as $$
declare
  current_request public.distribution_requests;
  changed public.distribution_requests;
begin
  select * into current_request
  from public.distribution_requests
  where id = target_request_id
  for update;

  if current_request.id is null then
    raise exception 'Delivery not found';
  end if;

  if not public.is_admin() and current_request.assigned_driver_id <> auth.uid() then
    raise exception 'Only the assigned driver can update this delivery';
  end if;

  if next_status not in ('heading_to_pickup', 'picked_up', 'out_for_delivery', 'delivered', 'not_delivered') then
    raise exception 'Unsupported delivery status';
  end if;

  if next_status = 'heading_to_pickup' and current_request.status <> 'driver_assigned' then
    raise exception 'Delivery must be assigned before pickup starts';
  end if;

  if next_status = 'picked_up' and current_request.status not in ('driver_assigned', 'heading_to_pickup') then
    raise exception 'Driver must be heading to pickup before confirming pickup';
  end if;

  if next_status = 'out_for_delivery' and current_request.status <> 'picked_up' then
    raise exception 'Delivery must be picked up before starting the route';
  end if;

  if next_status in ('delivered', 'not_delivered') and current_request.status <> 'out_for_delivery' then
    raise exception 'Delivery is not ready to be completed';
  end if;

  update public.distribution_requests
  set status = next_status
  where id = target_request_id
  returning * into changed;

  insert into public.delivery_events (request_id, actor_id, event_type, from_status, to_status)
  values (changed.id, auth.uid(), 'status_changed', current_request.status, changed.status);

  return changed;
end;
$$;

create or replace function public.activate_season(season_name text, season_starts_on date, season_ends_on date)
returns public.seasons
language plpgsql
security definer
set search_path = ''
as $$
declare
  activated public.seasons;
begin
  if not public.is_admin() then
    raise exception 'Only admins can manage seasons';
  end if;

  if length(trim(season_name)) = 0 then
    raise exception 'Season name is required';
  end if;

  if season_ends_on is not null and season_starts_on is not null and season_ends_on < season_starts_on then
    raise exception 'Season end date must be after its start date';
  end if;

  update public.seasons set is_active = false where is_active;
  insert into public.seasons (name, starts_on, ends_on, is_active)
  values (trim(season_name), season_starts_on, season_ends_on, true)
  returning * into activated;

  return activated;
end;
$$;

