create extension if not exists pgcrypto;

create type public.request_status as enum (
  'submitted',
  'under_review',
  'approved',
  'driver_assigned',
  'heading_to_pickup',
  'picked_up',
  'out_for_delivery',
  'delivered',
  'not_delivered',
  'denied'
);

create type public.driver_application_status as enum ('pending', 'approved', 'denied');

create table public.seasons (
  id uuid primary key default gen_random_uuid(),
  name text not null check (length(trim(name)) > 0),
  starts_on date,
  ends_on date,
  is_active boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (ends_on is null or starts_on is null or ends_on >= starts_on)
);

create unique index seasons_one_active_idx on public.seasons (is_active) where is_active;

create table public.driver_applications (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null unique references auth.users(id) on delete cascade,
  name text not null check (length(trim(name)) > 0),
  phone text not null check (length(trim(phone)) > 0),
  email text not null check (length(trim(email)) > 0),
  status public.driver_application_status not null default 'pending',
  reviewed_by uuid references auth.users(id) on delete set null,
  reviewed_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create unique index driver_applications_email_idx on public.driver_applications (lower(email));

create table public.distribution_requests (
  id uuid primary key default gen_random_uuid(),
  request_number bigint generated by default as identity (start with 1000),
  owner_id uuid not null references auth.users(id) on delete restrict,
  season_id uuid not null references public.seasons(id) on delete restrict,
  recipient_name text not null check (length(trim(recipient_name)) > 0),
  phone text not null check (length(trim(phone)) > 0),
  email text not null check (length(trim(email)) > 0),
  address text not null check (length(trim(address)) > 0),
  household_size integer not null check (household_size > 0),
  box_weight_lbs integer not null check (box_weight_lbs > 0),
  instructions text not null,
  status public.request_status not null default 'submitted',
  assigned_driver_id uuid references public.driver_applications(user_id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (owner_id, season_id)
);

create unique index distribution_requests_season_email_idx
  on public.distribution_requests (season_id, lower(email));
create unique index distribution_requests_season_phone_idx
  on public.distribution_requests (season_id, regexp_replace(phone, '[^0-9]', '', 'g'));
create index distribution_requests_status_idx on public.distribution_requests (season_id, status);
create index distribution_requests_driver_idx on public.distribution_requests (assigned_driver_id, status);

create table public.delivery_events (
  id bigint generated always as identity primary key,
  request_id uuid not null references public.distribution_requests(id) on delete cascade,
  actor_id uuid references auth.users(id) on delete set null,
  event_type text not null check (length(trim(event_type)) > 0),
  from_status public.request_status,
  to_status public.request_status,
  notes text,
  created_at timestamptz not null default now()
);

create index delivery_events_request_idx on public.delivery_events (request_id, created_at desc);

create or replace function public.set_updated_at()
returns trigger
language plpgsql
set search_path = ''
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

