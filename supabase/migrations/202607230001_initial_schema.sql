before update on public.seasons
for each row execute function public.set_updated_at();

create trigger driver_applications_set_updated_at
before update on public.driver_applications
for each row execute function public.set_updated_at();

create trigger distribution_requests_set_updated_at
before update on public.distribution_requests
for each row execute function public.set_updated_at();

create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = ''
as $$
  select coalesce((auth.jwt() -> 'app_metadata' ->> 'role') = 'admin', false);
$$;

create or replace function public.is_approved_driver(candidate uuid default auth.uid())
returns boolean
language sql
stable
security definer
set search_path = ''
as $$
  select exists (
    select 1
    from public.driver_applications
    where user_id = candidate and status = 'approved'
  );
$$;

revoke all on function public.is_admin() from public;
revoke all on function public.is_approved_driver(uuid) from public;
grant execute on function public.is_admin() to authenticated;
grant execute on function public.is_approved_driver(uuid) to authenticated;

